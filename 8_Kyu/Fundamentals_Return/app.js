function add(a,b){
    return parseFloat(a + b)
 }
 
 function divide(a,b){
     return parseFloat( a / b);
 }
 
 function multiply(a,b){
     return parseFloat(a * b)
 }
 
 function mod(a,b){
     return parseFloat(a % b)
 }
    
 function exponent(a,b){
     return parseFloat(Math.pow(a, b))
 }
     
 function subt(a,b){
     return parseFloat(a - b)
 }