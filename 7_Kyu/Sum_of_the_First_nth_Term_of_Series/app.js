    let num = 1;
    if(n === 0) {
      return (num - 1).toFixed(2)
    } else {
      for(let i = 2; i < n + 1;  i++){
        (num += (1 / ((i * 2) + (i - 2))));
        }
        return num.toFixed(2);                                 
      }
